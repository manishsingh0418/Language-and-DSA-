class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count =0;
        int original=N;
        while(N>0)
        {
            int a=N%10;
            if(a!=0 && original%a==0)
            {
            count++;
            }
            N=N/10;
        }
        return count;
    }
};

